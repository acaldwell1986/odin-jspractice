let weekDay = prompt("What day of the week is it?")


switch (weekDay) {
    case 'Monday': alert("Ugh. I hate mondays.")
    break;
    case 'Tuesday': alert("Woo! Tacos.")
    break;
    case 'Wednesday': alert("Halfway done the week!")
    break;
    case 'Thursday': alert("Throwbacks if you must.")
    break;
    case 'Friday': alert("We're done for the week!!")
    break;
    case 'Saturday': alert("Time for chores and then resting.")
    break;
    case 'Sunday': alert("Watching football!")
    break;
    default: alert("Please enter a valid day, my dude.")
}
